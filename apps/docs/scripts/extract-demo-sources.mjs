// Generates lib/demo-sources.generated.ts: for every demo id in the
// ObjectDemo registry, the demo's own JSX plus the FULL source of every
// screen/art component it renders (transitively), so the docs can show the
// complete, copyable TSX behind each mockup. Run automatically before
// `next dev` / `next build`; the output is committed so typecheck works
// without running it.
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const read = (p) => readFileSync(resolve(root, p), 'utf8')

/** Files whose top-level function components count as screen content. */
const ART_FILES = [
  'components/screens/print-art.tsx',
  'components/screens/watch-face.tsx',
  'components/screens/tap-counter.tsx',
  'components/screens/lock-screen.tsx',
  'components/screens/desktop-screen.tsx',
  'components/screens/music-player.tsx',
  'components/screens/stride-app.tsx',
  'components/screens/embedded-screen.tsx',
  'components/object-examples.tsx',
]

/**
 * Extract every top-level `function Name(...)` (exported or not) with its
 * leading jsdoc. Components in these files close with a lone `}` in column
 * 0, which keeps the scan simple and robust.
 */
function extractComponents(source) {
  const lines = source.split('\n')
  const out = new Map()
  for (let i = 0; i < lines.length; i++) {
    const m = lines[i].match(/^(?:export )?function ([A-Z][A-Za-z0-9_]*)\(/)
    if (!m) continue
    // include the jsdoc block immediately above, if any
    let start = i
    if (lines[i - 1]?.trim() === '*/') {
      for (let j = i - 1; j >= 0; j--) {
        if (lines[j].trimStart().startsWith('/**')) {
          start = j
          break
        }
      }
    }
    let end = i
    for (let j = i + 1; j < lines.length; j++) {
      if (lines[j] === '}') {
        end = j
        break
      }
    }
    out.set(m[1], lines.slice(start, end + 1).join('\n'))
    i = end
  }
  return out
}

const components = new Map()
for (const file of ART_FILES) {
  for (const [name, code] of extractComponents(read(file))) {
    if (!components.has(name)) components.set(name, code)
  }
}
// Registry plumbing, not screen content.
for (const name of ['ObjectDemo']) components.delete(name)

// --- demo registry entries: `  'id': (` ... `  ),` ---
const registry = read('components/object-examples.tsx')
const demoRe = /^ {2}'([a-z0-9-]+)': \($/
const regLines = registry.split('\n')
const demos = new Map()
for (let i = 0; i < regLines.length; i++) {
  const m = regLines[i].match(demoRe)
  if (!m) continue
  for (let j = i + 1; j < regLines.length; j++) {
    if (regLines[j] === '  ),') {
      // dedent the JSX by its registry indentation
      const jsx = regLines
        .slice(i + 1, j)
        .map((l) => (l.startsWith('    ') ? l.slice(4) : l))
        .join('\n')
      demos.set(m[1], jsx)
      i = j
      break
    }
  }
}

/** Names referenced by a source snippet, resolved transitively. */
function referenced(code, into = new Set()) {
  for (const name of components.keys()) {
    if (into.has(name)) continue
    if (new RegExp(`<${name}[\\s/>]|\\{${name}\\}|<${name}$`, 'm').test(code)) {
      into.add(name)
      referenced(components.get(name), into)
    }
  }
  return into
}

const entries = []
for (const [id, jsx] of demos) {
  const parts = [{ name: 'Demo', code: jsx }]
  for (const name of referenced(jsx)) {
    parts.push({ name, code: components.get(name) })
  }
  entries.push(`  ${JSON.stringify(id)}: [\n${parts
    .map((p) => `    { name: ${JSON.stringify(p.name)}, code: ${JSON.stringify(p.code)} }`)
    .join(',\n')},\n  ]`)
}

const output = `/* eslint-disable */
// AUTO-GENERATED by scripts/extract-demo-sources.mjs — do not edit by hand.
// Maps each ObjectDemo id to its JSX plus the full source of every screen
// component it renders, for the "view source" panel under each demo.

export interface DemoSourcePart {
  name: string
  code: string
}

export const DEMO_SOURCES: Record<string, DemoSourcePart[]> = {
${entries.join(',\n')},
}
`
writeFileSync(resolve(root, 'lib/demo-sources.generated.ts'), output)
console.log(`extracted ${demos.size} demos, ${components.size} components`)
